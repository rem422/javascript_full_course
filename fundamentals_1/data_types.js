//Data Types
// object and premitive data types

//Premitive data types:

//1. Numbers - floating point numbers, used for decimals and integers.
//1. Strings - Sequence of characters, used for text.
//3. Booleans - Logical type that only can be true or false, used for taking decisions.
//4. Undefined - Value taken by variable that is not yet defined ("empty value").
//5. Null - Also means 'empty value.
//6. Symbol (ES2015) - Value that is unique and cannot be changed [Not usefull for now].
//7. Big Int (ES2020) - Larger integers that the number type can not hold.

// JavaScript has dynamic typing: We do not have to manually define the type of the value stored in a variable. Instead, data types are determined automatically.

let javascriptIsFun = true;
console.log(javascriptIsFun);

console.log(typeof javascriptIsFun);
console.log(typeof 23);
console.log(typeof "Jonas");

javascriptIsFun = 'YES!';
console.log(typeof javascriptIsFun);

let myName;
console.log(myName);
console.log(typeof myName);

myName = 'Rem Simiyu';
console.log(typeof myName);

console.log(typeof null);





